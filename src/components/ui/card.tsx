import * as React from "react"

import { cn } from "#/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card flex flex-col gap-4 overflow-hidden rounded-lg border border-border bg-card text-sm text-card-foreground transition-shadow hover:shadow-sm has-data-[slot=card-footer]:pb-0 has-[>img:first-child]:pt-0 data-[size=default]:p-5 data-[size=sm]:gap-3 data-[size=sm]:p-4",
        className
      )}
      {...props}
    />
  )
}

